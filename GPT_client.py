# -----------------------------------
# Function: Get Snake Description
# -----------------------------------
def get_snake_description(species_name: str) -> str:

    if client is None:
        return "LLM service unavailable"

    system_prompt = (
        "You are a professional snake species expert and herpetologist. "
        "Provide accurate, scientific, and concise descriptions of snake species."
    )

    user_prompt = (
        f"Describe the snake species '{species_name}' including:\n"
        f"- Venom type and danger level\n"
        f"- Habitat and geographic distribution\n"
        f"- Behavior and typical environment\n"
        f"- Conservation or ecological importance\n\n"
        f"Provide a single short paragraph (max 20 words)."
    )

    try:
        response = client.chat.completions.create(
            model="openai/gpt-oss-20b",
            messages=[
                {"role": "system", "content": system_prompt},
                {"role": "user", "content": user_prompt}
            ],
            temperature=0.2,
            stream=False
        )

        return response.choices[0].message.content.strip()

    except Exception as e:
        return f"Error generating description: {str(e)}"


