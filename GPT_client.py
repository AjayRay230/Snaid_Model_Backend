# -----------------------------------
# Metadata Mapping Function
# -----------------------------------
def get_snake_metadata(parsed):

    species_name = parsed["species_name"]

    row = df[df["binomial"].str.lower() == species_name.lower()]

    if row.empty:
        return {
            "Venomous status": "Unknown",
            "Recommended Antivenom": "Not Available"
        }

    row = row.iloc[0]

    venom_status = "Venomous" if row["venom_status"] == 1 else "Non-Venomous"
    antivenom = row["antivenom Name"] if row["antivenom Name"] != "Not applicable" else "None"
    habitat = row.get("region", "Not available")

    return {
        "species": species_name,
        "confidence": parsed["confidence"],
        "venom_status": venom_status,
        "antivenom": antivenom,
        "habitat": habitat,
        "about": parsed["about_snake"]
    }


# -----------------------------------
# LLM Snake Identifier
# -----------------------------------
def LLM_snake_identifier(description: str) -> dict:

    if client is None:
        return {"error": "LLM service unavailable"}

    system_prompt = (
        "You are a professional snake taxonomy expert.\n"
        "You MUST choose species_name strictly from the provided allowed list.\n"
        "Return ONLY valid JSON."
    )

    allowed_species_text = "\n".join(ALLOWED_SPECIES)

    user_prompt = f"""
Allowed species list:
{allowed_species_text}
Return JSON:
{{
  "species_name": "...",
  "confidence": "85%",
  "about_snake": "short description"
}}
Snake description:
\"\"\"{description}\"\"\"
"""

    try:
        response = client.chat.completions.create(
            model="openai/gpt-oss-20b",
            messages=[
                {"role": "system", "content": system_prompt},
                {"role": "user", "content": user_prompt}
            ],
            temperature=0.1,
            stream=False
        )

        raw_output = response.choices[0].message.content.strip()

        # Extract JSON safely
        start = raw_output.find("{")
        end = raw_output.rfind("}") + 1

        if start == -1 or end == -1:
            raise ValueError("Invalid JSON output")

        parsed = json.loads(raw_output[start:end])

        # Validation
        if parsed["species_name"] not in ALLOWED_SPECIES:
            raise ValueError("Invalid species returned")

        return get_snake_metadata(parsed)

    except Exception as e:
        return {"error": str(e)}